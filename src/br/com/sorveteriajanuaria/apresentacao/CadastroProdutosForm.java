    private void salvarProduto(){
        try {
            this.validarCamposObrigatorios();
            this.recuperarCamposTela();

            ProdutosBO produtosBO = new ProdutosBO();
            String tituloMensagem;

            if (produtos.getIdProdutos() == -1) {
               tituloMensagem = "Cadastro de produtos";
               produtosBO.inserir(produtos);
               JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", tituloMensagem, JOptionPane.INFORMATION_MESSAGE);
            } else {
                tituloMensagem = "Editar produto";
                produtosBO.alterar(produtos);
                JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!", tituloMensagem, JOptionPane.INFORMATION_MESSAGE);
                if (pesquisaProdutosForm == null) {
                    pesquisaProdutosForm = new PesquisaProdutosForm();
                }
                this.fecharCadastroProdutos();
            }
            this.limparCamposTela();
        } catch (SorveteriaJanuariaException e) {
            String mensagem = "Erro ao realizar cadastro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de produto", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de produto", JOptionPane.ERROR_MESSAGE);
        }
    }
    
