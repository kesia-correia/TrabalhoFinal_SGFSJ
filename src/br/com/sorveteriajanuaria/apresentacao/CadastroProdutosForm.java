    private void inicializarCamposTela(){
        this.txtNome.setText(this.produtos.getNome());
        this.txtSabor.setText(this.produtos.getSabor());
         DecimalFormat formatadorDecimal = new DecimalFormat("#,##0.00");
        this.txtPreco.setText(
                formatadorDecimal.format(this.produtos.getPreco()));
    }
    
     private void limparCamposTela() {
       this.txtNome.setText("");
       this.txtSabor.setText("");
       this.txtPreco.setText("");
     }
     
      private void recuperarCamposTela() throws ParseException {
        if (!txtNome.getText().trim().equals("")) {
            produtos.setNome(txtNome.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        
        if (!txtSabor.getText().trim().equals("")) {
            produtos.setSabor(txtSabor.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        
         String preco = txtPreco.getText();
        if (!preco.equals("")) {
            DecimalFormat decimalFormat = new DecimalFormat("#.00");
            produtos.setPreco(decimalFormat.parse(preco).doubleValue());
        }else{
            throw new CampoObrigatorioException();
        }
    }
      
     private void fecharCadastroProdutos(){
        CadastroProdutosForm.this.dispose();
        if (pesquisaProdutosForm == null) {
           pesquisaProdutosForm = new PesquisaProdutosForm();
        }
       pesquisaProdutosForm.setVisible(true);
       pesquisaProdutosForm.toFront();
    } 
     
     private void validarCamposObrigatorios() {
        if (txtNome.getText().trim().isEmpty()
                || txtSabor.getText().trim().isEmpty()
                || txtPreco.getText().trim().isEmpty()){
            throw new CampoObrigatorioException();
        }
    }

