    private void inicializarCamposTela(){
        this.txtNome.setText(this.fornecedor.getNome());
        this.txtCNPJ.setText(this.fornecedor.getCNPJ());
        this.txtTelefone.setText(this.fornecedor.getTelefone());
        this.txtEndereco.setText(this.fornecedor.getEndereco());
        this.txtObservacoes.setText(this.fornecedor.getObservacoes());
    }
    
     private void limparCamposTela() {
       this.txtNome.setText("");
       this.txtCNPJ.setText("");
       this.txtTelefone.setText("");
       this.txtEndereco.setText("");
       this.txtObservacoes.setText("");
     }
     
     private void recuperarCamposTela() throws ParseException {
        if (!txtNome.getText().trim().equals("")) {
            fornecedor.setNome(txtNome.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        
        if (!txtCNPJ.getText().trim().equals("")) {
            fornecedor.setCNPJ(txtCNPJ.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        
        if (!txtTelefone.getText().trim().equals("")) {
            fornecedor.setTelefone(txtTelefone.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        
        if (!txtEndereco.getText().trim().equals("")) {
            fornecedor.setEndereco(txtEndereco.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        
        if (!txtObservacoes.getText().trim().equals("")) {
            fornecedor.setObservacoes(txtObservacoes.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
     }
     
     private void fecharCadastroFornecedores(){
        CadastroFornecedoresForm.this.dispose();
        if (pesquisaFornecedoresForm == null) {
           pesquisaFornecedoresForm = new PesquisaFornecedoresForm();
        }
      pesquisaFornecedoresForm.setVisible(true);
       pesquisaFornecedoresForm.toFront();
    } 
    
     private void validarCamposObrigatorios() {
        if (txtNome.getText().trim().isEmpty()
                || txtCNPJ.getText().trim().isEmpty()
                || txtTelefone.getText().trim().isEmpty()
                || txtEndereco.getText().trim().isEmpty()
                || txtObservacoes.getText().trim().isEmpty()){
            throw new CampoObrigatorioException();
        }
    }
     
