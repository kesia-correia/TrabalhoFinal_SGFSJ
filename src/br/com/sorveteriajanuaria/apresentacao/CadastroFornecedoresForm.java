    private void salvarProdutos(){
         try {
            this.validarCamposObrigatorios();
            this.recuperarCamposTela();

            FornecedorBO fornecedorBO = new  FornecedorBO();
            String tituloMensagem;

            if (fornecedor.getIdFornecedor() == -1) {
               tituloMensagem = "Cadastro de fornecedor";
               fornecedorBO.inserir(fornecedor);
               JOptionPane.showMessageDialog(this, "Fornecedor cadastrado com sucesso!", tituloMensagem, JOptionPane.INFORMATION_MESSAGE);
            } else {
                tituloMensagem = "Editar fornecedor";
                fornecedorBO.alterar(fornecedor);
                JOptionPane.showMessageDialog(this, "Fornecedor alterado com sucesso!", tituloMensagem, JOptionPane.INFORMATION_MESSAGE);
                if (pesquisaFornecedoresForm == null) {
                    pesquisaFornecedoresForm = new PesquisaFornecedoresForm();
                }
                this.fecharCadastroFornecedores();
            }
            this.limparCamposTela();
        } catch (SorveteriaJanuariaException e) {
            String mensagem = "Erro ao realizar cadastro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de fornecedor", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de fornecedor", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
